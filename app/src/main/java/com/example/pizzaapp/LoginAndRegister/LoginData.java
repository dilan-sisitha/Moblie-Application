package com.example.pizzaapp.LoginAndRegister;

public class LoginData {

    int loginId;
    String email;


    public int getLoginId() {
        return loginId;
    }

    public void setLoginId(int loginId) {
        this.loginId = loginId;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
